#include<stdio.h>

void main11(){
	//비교 연산자 (1: 맞다, 0:아니다)
	int num1, num2;

	scanf_s("%d", &num1);
	scanf_s("%d", &num2);
	//1.같다
	printf("%d", num1 == num2);

	//2. 다르다
	printf("%d", num1 != num2);

	//3. 작다.
	printf("%d", num1 <num2);

	//4. 작거나 같다
	printf("%d", num1 <= num2);

	//5. 크다.
	printf("%d", num1 > num2);

	//6. 크거나 같다.
	printf("%d", num1 >= num2);

	






	
}